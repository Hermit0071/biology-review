export function validateContent() {
  const errors: string[] = []
  if (lessons.length !== 33) errors.push(`课程数量应为 33，当前为 ${lessons.length}`)
  const ids = new Set<string>()
  lessons.forEach((l) => {
    if (ids.has(l.id)) errors.push(`重复课程 ID：${l.id}`); ids.add(l.id)
    const c = l.content
    if (!l.pages || !l.topics.length) errors.push(`${l.id} 缺少页码或专题`)
    if ([c.goals,c.concepts,c.process,c.experiments,c.pitfalls,c.examTips,c.network].some((x) => !x.length)) errors.push(`${l.id} 存在空知识板块`)
    if (l.modelId && !models.some((m) => m.id === l.modelId)) errors.push(`${l.id} 模型不存在`)
  })
  models.forEach((m) => { if (!lessons.some((l) => l.id === m.lessonId)) errors.push(`${m.id} 未关联课程`) })
  return errors
}
